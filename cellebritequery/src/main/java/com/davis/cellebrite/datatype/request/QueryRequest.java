package com.davis.cellebrite.datatype.request;

public class QueryRequest {
	private String tag;
	private String field;
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	
}
